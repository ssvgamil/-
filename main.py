# пятиугольник
def pytyg(x, y, a, l):
    l5 = turtle.Turtle()
    l5.up()
    l5.begin_fill()
    l5.fillcolor(str(l))
    l5.goto(x, y)
    l5.pd()
    l5.fd(a)
    l5.lt(72)
    l5.fd(a)
    l5.lt(72)
    l5.fd(a)
    l5.lt(72)
    l5.fd(a)
    l5.lt(72)
    l5.fd(a)
    l5.lt(72)
    l5.fd(a)
    l5.up()
    l5.end_fill()


# Прямоугольный треугольник
def trpr(x, y, a, b, ug, l):
    pt3 = turtle.Turtle()
    pt3.up()
    pt3.goto(x, y)
    pt3.begin_fill()
    pt3.fillcolor(str(l))
    pt3.pd()
    pt3.rt(ug)
    pt3.fd(a)
    pt3.goto(x, y)
    pt3.lt(90)
    pt3.fd(b)
    pt3.goto(x + a, y)
    pt3.up()
    pt3.end_fill()


